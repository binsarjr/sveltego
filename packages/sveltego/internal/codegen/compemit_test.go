// TestEmitComponentTree_SimpleChild verifies that a +page.svelte importing
// one sibling component emits a component.gen.go for that component.
func TestEmitComponentTree_SimpleChild(t *testing.T) {
	t.Parallel()
	root := t.TempDir()
	writeFile(t, filepath.Join(root, "go.mod"), "module example.com/app\n\ngo 1.22\n")

	writeFile(t, filepath.Join(root, "src", "routes", "button.svelte"),
		`<span>{props.Label}</span>`+"\n")
	writeFile(t, filepath.Join(root, "src", "routes", "+page.svelte"),
		`<script lang="go">
import button "./button"
</script>
<button.Button label="OK" />
`)
	if _, err := Build(BuildOptions{ProjectRoot: root}); err != nil {
		t.Fatalf("Build: %v", err)
	}

	genFile := filepath.Join(root, ".gen", "components", "routes", "button", "component.gen.go")
	if _, err := os.Stat(genFile); err != nil {
		t.Errorf("expected %s to exist: %v", genFile, err)
	}
	data, err := os.ReadFile(genFile)
	if err != nil {
		t.Fatalf("read component gen: %v", err)
	}
	for _, want := range []string{
		"// Code generated by sveltego. DO NOT EDIT.",
		"package button",
		"type Button struct{}",
		"type ButtonProps = struct{}",
		"type ButtonSlots struct",
	} {
		if !bytes.Contains(data, []byte(want)) {
			t.Errorf("component.gen.go missing %q:\n%s", want, data)
		}
	}
	assertParsesAsGo(t, genFile)
}

// TestEmitComponentTree_NestedChild verifies that a component importing a
// grandchild results in both being emitted.
func TestEmitComponentTree_NestedChild(t *testing.T) {
	t.Parallel()
	root := t.TempDir()
	writeFile(t, filepath.Join(root, "go.mod"), "module example.com/app\n\ngo 1.22\n")

	// grandchild: icon.svelte
	writeFile(t, filepath.Join(root, "src", "routes", "icon.svelte"),
		`<svg></svg>`+"\n")
	// child: button.svelte imports icon
	writeFile(t, filepath.Join(root, "src", "routes", "button.svelte"),
		`<script lang="go">
import icon "./icon"
</script>
<icon.Icon /><span>{props.Label}</span>
`)
	// page imports button
	writeFile(t, filepath.Join(root, "src", "routes", "+page.svelte"),
		`<script lang="go">
import button "./button"
</script>
<button.Button label="OK" />
`)

	if _, err := Build(BuildOptions{ProjectRoot: root}); err != nil {
		t.Fatalf("Build: %v", err)
	}

	for _, rel := range []string{
		filepath.Join(".gen", "components", "routes", "button", "component.gen.go"),
		filepath.Join(".gen", "components", "routes", "icon", "component.gen.go"),
	} {
		genFile := filepath.Join(root, rel)
		if _, err := os.Stat(genFile); err != nil {
			t.Errorf("expected %s to exist: %v", genFile, err)
		}
		assertParsesAsGo(t, genFile)
	}
}

