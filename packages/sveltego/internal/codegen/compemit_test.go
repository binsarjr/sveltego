// TestEmitComponentTree_Cycle verifies that a component cycle (A→B→A) causes
// Build to return a descriptive error.
func TestEmitComponentTree_Cycle(t *testing.T) {
	t.Parallel()
	root := t.TempDir()
	writeFile(t, filepath.Join(root, "go.mod"), "module example.com/app\n\ngo 1.22\n")

	// a.svelte imports b.svelte, b.svelte imports a.svelte
	writeFile(t, filepath.Join(root, "src", "routes", "a.svelte"),
		`<script lang="go">
import b "./b"
</script>
<b.B />
`)
	writeFile(t, filepath.Join(root, "src", "routes", "b.svelte"),
		`<script lang="go">
import a "./a"
</script>
<a.A />
`)
	writeFile(t, filepath.Join(root, "src", "routes", "+page.svelte"),
		`<script lang="go">
import a "./a"
</script>
<a.A />
`)

	_, err := Build(BuildOptions{ProjectRoot: root})
	if err == nil {
		t.Fatal("expected error for component cycle, got nil")
	}
	if !strings.Contains(err.Error(), "cycle") {
		t.Errorf("expected 'cycle' in error, got: %v", err)
	}
}

// TestEmitComponentTree_CasingCollision verifies that two .svelte files
// differing only in case (Card.svelte vs card.svelte) in the same directory
// cause Build to return a casing collision error.
func TestEmitComponentTree_CasingCollision(t *testing.T) {
	t.Parallel()
	root := t.TempDir()
	writeFile(t, filepath.Join(root, "go.mod"), "module example.com/app\n\ngo 1.22\n")

	// On case-insensitive filesystems, creating both files may overwrite one.
	// We simulate the collision by adding them both to the adjacency map via
	// a page that imports both via distinct relative paths.
	// Note: this test is meaningful on case-sensitive filesystems (Linux/CI).
	// On macOS the filesystem may merge them; we guard by checking the error
	// only when both stat as different inodes.
	cardUpper := filepath.Join(root, "src", "routes", "Card.svelte")
	cardLower := filepath.Join(root, "src", "routes", "card.svelte")

	writeFile(t, cardUpper, `<div>Card</div>`+"\n")
	// Try to write the second file; if they resolve to the same path (case-
	// insensitive FS), the test is moot.
	writeFile(t, cardLower, `<div>card</div>`+"\n")

	infoUpper, errU := os.Stat(cardUpper)
	infoLower, errL := os.Stat(cardLower)
	if errU != nil || errL != nil {
		t.Skip("cannot stat both casing variants")
	}
	if os.SameFile(infoUpper, infoLower) {
		t.Skip("case-insensitive filesystem: Card.svelte and card.svelte are the same file")
	}

	writeFile(t, filepath.Join(root, "src", "routes", "+page.svelte"),
		`<script lang="go">
import card1 "./Card"
import card2 "./card"
</script>
<card1.Card />
<card2.Card />
`)

	_, err := Build(BuildOptions{ProjectRoot: root})
	if err == nil {
		t.Fatal("expected casing collision error, got nil")
	}
	if !strings.Contains(err.Error(), "casing collision") && !strings.Contains(err.Error(), "collision") {
		t.Errorf("expected 'casing collision' in error, got: %v", err)
	}
}

