func TestEncodeComponentPath(t *testing.T) {
	t.Parallel()
	cases := []struct {
		projectRoot string
		absPath     string
		want        string
	}{
		{
			projectRoot: "/app",
			absPath:     "/app/src/routes/button.svelte",
			want:        "routes/button",
		},
		{
			projectRoot: "/app",
			absPath:     "/app/src/routes/foo/bar/MyCard.svelte",
			want:        "routes/foo/bar/mycard",
		},
		{
			projectRoot: "/app",
			absPath:     "/app/src/routes/[id]/Widget.svelte",
			want:        "routes/_id_/widget",
		},
		{
			projectRoot: "/app",
			absPath:     "/app/src/lib/ui/Button.svelte",
			want:        "lib/ui/button",
		},
	}
	for _, tc := range cases {
		got := encodeComponentPath(tc.projectRoot, tc.absPath)
		if got != tc.want {
			t.Errorf("encodeComponentPath(%q, %q) = %q, want %q", tc.projectRoot, tc.absPath, got, tc.want)
		}
	}
}

