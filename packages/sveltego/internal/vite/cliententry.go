package vite

import (
	"fmt"
	"strings"
)

// GenerateClientEntry returns the contents of a stub entry.ts file for a
// route. relSveltePath is the path to the .svelte file relative to the
// directory that will contain entry.ts (so callers must compute the
// appropriate "../../../..." prefix).
//
// The stub imports the Svelte component and emits a console.log so the
// client bundle is non-empty. Actual hydration runtime is wired in #36.
func GenerateClientEntry(relSveltePath string) string {
	var b strings.Builder
	b.WriteString("// Code generated by sveltego — DO NOT EDIT.\n")
	b.WriteString("// Hydration runtime wiring lands in #36.\n\n")
	fmt.Fprintf(&b, "import App from %q;\n", relSveltePath)
	b.WriteString("\nconsole.log('[sveltego] client entry loaded', App);\n")
	return b.String()
}

// RelativeSveltePath returns the relative path from a generated client entry
// directory to the .svelte source file at svelteSrcFromRoot (relative to
// project root). depth is how many directory levels the entry dir sits below
// the project root (e.g. .gen/client/routes/+page → depth=4).
func RelativeSveltePath(svelteSrcFromRoot string, depth int) string {
	prefix := strings.Repeat("../", depth)
	return prefix + svelteSrcFromRoot
}
